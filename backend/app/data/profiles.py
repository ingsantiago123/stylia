def get_preset(name: str) -> dict | None:
    """Retorna un preset por nombre, o None si no existe."""
    preset = PRESETS.get(name)
    if preset is None:
        return None
    # Retornar copia sin campos de UI
    return {k: v for k, v in preset.items() if not k.startswith("ui_")}


def get_preset_ui_info(name: str) -> dict | None:
    """Retorna la info de UI de un preset."""
    preset = PRESETS.get(name)
    if preset is None:
        return None
    return {
        "key": name,
        "name": preset.get("ui_name", name),
        "description": preset.get("ui_description", ""),
        "icon": preset.get("ui_icon", "file-text"),
        "intervention_level": preset.get("intervention_level", "moderada"),
        "register": preset.get("register", "neutro"),
    }


def list_presets_ui() -> list[dict]:
    """Lista todos los presets con info de UI."""
    return [get_preset_ui_info(k) for k in PRESETS]
